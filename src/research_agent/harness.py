def _deserialize_conversation(
    serialized: list[dict[str, Any]],
) -> list[dict[str, Any]]:
    """Convert session.json's serialized conversation back to live message format.

    Session.json serializes tool_calls as:
        {"id": "...", "name": "...", "arguments": {...}}

    The live loop format (matching litellm/OpenAI) expects:
        {"id": "...", "type": "function", "function": {"name": "...", "arguments": "<json-string>"}}

    This function converts the serialized format back so the loop can
    continue appending new messages in the correct format.
    """
    messages: list[dict[str, Any]] = []
    for msg in serialized:
        entry: dict[str, Any] = {"role": msg["role"]}

        if msg.get("content") is not None:
            entry["content"] = msg["content"]

        if msg.get("tool_call_id") is not None:
            entry["tool_call_id"] = msg["tool_call_id"]

        if "tool_calls" in msg:
            entry["tool_calls"] = [
                {
                    "id": tc["id"],
                    "type": "function",
                    "function": {
                        "name": tc["name"],
                        "arguments": json.dumps(tc["arguments"]),
                    },
                }
                for tc in msg["tool_calls"]
            ]

        messages.append(entry)
    return messages


def _extract_simulated_user_history(
    conversation: list[dict[str, Any]],
    system_message: dict[str, str],
) -> list[dict[str, str]]:
    """Reconstruct SimulatedUser's conversation_history from escalation exchanges.

    The SimulatedUser's conversation_history is structured as:
        [system_message, user_msg_1, assistant_msg_1, user_msg_2, assistant_msg_2, ...]

    Where each (user, assistant) pair corresponds to one escalate_to_user call:
    - user = the "summary" argument passed to escalate_to_user
    - assistant = the tool result (the simulated user's response)

    This function scans the serialized main conversation for escalate_to_user
    tool calls and their corresponding tool results, rebuilding the history.

    Args:
        conversation: Serialized main_conversation from session.json.
        system_message: The SimulatedUser's system message (constructed
            at init time from the research brief).

    Returns:
        Reconstructed conversation_history list suitable for
        SimulatedUser.restore_history().
    """
    history: list[dict[str, str]] = [system_message]

    # Build a map: tool_call_id -> summary text (from escalate_to_user calls)
    escalation_calls: dict[str, str] = {}
    for msg in conversation:
        for tc in msg.get("tool_calls", []):
            if tc.get("name") == "escalate_to_user":
                args = tc.get("arguments", {})
                summary = args.get("summary", "")
                call_id = tc.get("id")
                if call_id:
                    escalation_calls[call_id] = summary

    # Match tool results to escalation calls, preserving conversation order
    for msg in conversation:
        if msg.get("role") != "tool":
            continue
        call_id = msg.get("tool_call_id")
        if call_id in escalation_calls:
            # user turn = the summary the main agent sent
            history.append({"role": "user", "content": escalation_calls[call_id]})
            # assistant turn = the simulated user's response
            history.append({"role": "assistant", "content": msg.get("content", "")})

    return history
